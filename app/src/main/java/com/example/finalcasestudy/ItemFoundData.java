package com.example.finalcasestudy;

public class ItemFoundData{
    private String name;
    private String date;

    public ItemFoundData() {
    }

    public ItemFoundData(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }
}
